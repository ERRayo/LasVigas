					La problemática actual respecto a la contaminación y cambio climático ha hecho que el medio ambiente esté en boca de todos y haya aumentado la preocupación de los ciudadanos por las posibles consecuencias que tiene un tratamiento nocivo al medio que nos rodea.
				</p><br>
				<p align="justify">
					Al ser un tema de actualidad y que nos concierne a todos, es importante que sea explicado y conseguir que llegue a toda la población, por ello se crean campañas de sensibilización y concienciación medioambiental. La educación es fundamental para conseguir los objetivos propuestos y por ello surge una disciplina que es la Educación Ambiental.
				</p><br>
				<br><h3>Concienciación ambiental</h3><br>
				<P align="justify">
					La Educación Ambiental es un proceso que dura toda la vida y que tiene como objetivo impartir conciencia ambiental, conocimiento ecológico, actitudes y valores hacia el medio ambiente para tomar un compromiso de acciones y responsabilidades que tengan por fin el uso racional de los recursos y poder lograr así un desarrollo adecuado y sostenible.
				
				</P>
				<br><p>Hazlo por tu hogar:</p><br>
				<ul>
					<li>	Toma de conciencia: concienciar a la gente de los problemas relacionados con el medio.</li>
					<li>	Conocimientos: ayudar a interesarse por el medio.</li>
					<li>	Actitudes: adquirir interés por el medio ambiente y voluntad para conservarlo.</li>
					<li>	Aptitudes: ayudar a adquirir aptitudes para resolver el problema.</li>
					<li>	Capacidad de evaluación: evaluar los programas de Educación Ambiental.</li>
					<li>	Participación: desarrollar el sentido de la responsabilidad para adoptar medidas adecuadas.</li>
				</ul>
				<br><br><p>Los mejores consejos para cuidar el medio ambiente</p><br>
				
				<ul >
					<li ><p align="justify"> Antes de comprar un producto, hazte la pregunta si realmente lo necesitas.<br>
					Cualquier consumo innecesario es en esencia antiecológico y antieconómico, ¿no lo crees?.
					Así vamos ahorrando un dinero que al cabo de un año será significativo.</p></li><br>
										<li><p align="justify"> Sé crítico con la publicidad.<br>
					Mira las cualidades de los productos, no los sueños que te venden en la publicidad muchas veces engañosa.</p></li><br>
										<li><p align="justify">Antes de tirar cualquier cosa a la basura, piensa si se puede reutilizar, reciclar o reparar, o si puede ser útil para otra persona.</p></li><br>
										<li><p align="justify">Evita las latas de bebidas, vale más el envase que su contenido y apenas se recuperan o reciclan.</p></li><br>
										<li><p align="justify">La energía que producen las pilas es más de 600 veces más cara que la de la red.<br>
					Conecta los aparatos a la red siempre que esto sea posible.
					Si te es imprescindible usar pilas (nunca tires las usadas a la basura), procura que sean recargables.</p></li><br>
										<li><p align="justify">Desconecta los aparatos eléctricos de la red cuando no están funcionando.<br>
					Algunos aparatos (como televisores) siguen gastando hasta un 33% de la energía.
					Se llaman “vampiros energéticos”, porque silenciosamente consumen energía si no están desenchufados.</p></li><br>
										<li><p align="justify">Prescinde de los electrodomésticos innecesarios como cepillos de dientes, abrelatas, cuchillos eléctricos, y demás chucherías que en vez de “facilitarnos” la vida, nos lleva a consumir y generar más basura electrónica.</p></li><br>
										<li><p align="justify">Usa bombillas LED que duran mucho más que las bombillas tradicionales y que inclusiva las fluorescentes.<br>
					Son de bajo consumo de energía para dar la misma cantidad de luz, con lo cual se termina ahorrando dinero y colaborando con el medio ambiente.</p></li><br>
										<li><p align="justify">Evita los aerosoles que contienen CFCs y que son causantes de la destrucción de la capa de ozono, u otros gases que también contribuyen al efecto invernadero.
					Los pulverizadores son una buena alternativa y son recargables.</p></li><br>
										<li><p align="justify">La gran mayoría de los productos de limpieza que se anuncian no sólo son innecesarios sino también muy nocivos para el medio ambiente.<br>
					Casi toda la casa puede quedar perfectamente limpia con jabón, bicarbonato, vinagre y limón.</p></li><br>
										<li><p align="justify">Los ambientadores no eliminan los malos olores, sino que desprenden otros más fuertes que nos impiden detectar los primeros.<br>
					Que éstos sean legítimamente ecológicos, biodegradables y libres de químicos agresivos.</p></li><br>
										<li><p align="justify">Evita usar productos de limpieza agresivos: limpiahornos, lejía, etc., que impiden los procesos biológicos de depuración del agua.</p></li><br>
										<li><p align="justify"> Para ahorrar agua, instala un sistema de doble descarga en el inodoro, ya que vaciar la cisterna entera supone gastar de 8 a 10 litros.</p></li><br>
