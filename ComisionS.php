			<h2 align="center">Comisión de Seguridad e Higiene de Las Vigas, Gro.<br></h2><br><br>
			<p align="justify">La comisión de seguridad e higiene de Las Vigas Guerrero es una comisión dedicada al cuidado y bienestar de la comunidad.<br><br>

				No obstante está organizada de forma bipartita, es decir, se forma por igual número de representantes de los trabajadores e igual número de representantes del patrón, y principalmente el objetivo de dicha comisión es de prevenir y vigilar que no sucedan accidentes de trabajo, proponer medidas preventivas, así como investigar las causas de los accidentes y enfermedades.<br> <br>
				Acontinuacion se presentan tres apartados para realizar registros de diversas indoles que se describen a continuación:<br> <br> 
			
			<b>Desastres naturales(Siniestro):</b> Si debido a un desastre natural se sufre alguna perdida es importantes registrar esa pérdida para tener conciencia de la magnitud de los daños ocasionados por un desastre natural y de esa forma poder realizar las métricas correspondientes.<br>En este apartado se deben registrar los siniestros sucedidos por dicho desastre natural, entiéndase por Siniestros como la avería, destrucción fortuita o pérdida importante que sufren las personas o la propiedad, y cuya materialización se traduce en una indemnización.<br><br>
			<b>Denuncia ciudadana:</b> En este apartado se deben registrar aquellos actos delictivos que afecten a terceros o a nuestra persona, ya se secuestró, robo, extorción, extravió, entre otras. Es importante recordar que todas la denuncias se pueden hacer de forma anónima, para esto se tiene que dejar en blanco el apartado de denunciante dentro del registro.<br><br>
			<b>Reporte civico:</b> Reportar problemas que nos afecten como sociedad es importante, para ello se creó el reporte cívico en el cual se puede realizar un reporte ya sea de una fuga de agua, un árbol caído o a punto de caer, un bache en la calle, una alcantarilla abierta, entre otros problemas de la misma índole.  <br></p>
